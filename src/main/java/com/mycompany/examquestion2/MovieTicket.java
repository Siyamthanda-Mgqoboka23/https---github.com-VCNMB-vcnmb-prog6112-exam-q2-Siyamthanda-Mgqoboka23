    private void processMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processMenuActionPerformed
     if (!validateInput()) {
        return;  // Exit if validation fails
    }
    String Movie = (String) cbMovies.getSelectedItem();
    String Tickets = InputTickets.getText();
    
    try {
        double ticketPrice = Double.parseDouble(InputTicketPrice.getText());
         double numberOftickets = Double.parseDouble(InputTickets.getText());
        
        // Calculate the technicianâ€™s commission
        double totalCost = ticketPrice/100 * numberOftickets * (100+14);
        
        String report = " MOVIE TICKET REPORT " + 
                         "\n"+
                        "\n. MOVIE NAME: " + Movie +
                        "\n. MOVIE TICKET PRICE: " + ticketPrice +
                        "\n. NUMBER OF TICKETS: " + Tickets + 
                        "\n. TOTAL TICKET PRICE: " + totalCost;
        
         // Display the formatted report in the output field
        outputArea.setText(report);
        
        } catch (NumberFormatException e) {
        // Handle any number format exceptions for ticketCost 
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values for ticket cost.");
    }
     try (FileWriter writer = new FileWriter("report.txt")) {
        String reportText = outputArea.getText();
        System.out.println(reportText);  // Temporary check
        writer.write(reportText);
        JOptionPane.showMessageDialog(this, "Report saved successfully!");
    } catch (IOException e) 
    {
        JOptionPane.showMessageDialog(this, "Error saving report: " + e.getMessage());
    }
    }//GEN-LAST:event_processMenuActionPerformed
 private boolean validateInput() {
    // Check if Tickets and name fields are filled
    if (cbMovies.getSelectedItem() == null || InputTickets.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in the Movie and Tickets.");
        return false;
    }
    
    try {
       double ticketPrice = Double.parseDouble(InputTicketPrice.getText());
  
        
        // Check if repair cost and rate are positive
        if (ticketPrice <= 0 || 14 <= 0) {
            JOptionPane.showMessageDialog(this, "Repair Cost and Rate must be greater than zero.");
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values for Repair Cost and Rate.");
        return false;
    }
    
    // All validations passed
    return true;
}
